// Uploaded bases name regions informally ("татарстан", "пермский",
// "ростовская") rather than by their full official name ("Республика
// Татарстан", "Пермский край") — strips the common administrative words
// from both sides so e.g. "татарстан" lines up with "татарстан" (from
// "Республика Татарстан" with "республика" stripped).
function normalizeRegionKey(s: string): string {
  return s
    .toLowerCase()
    .replace(/республика|автономный округ|автономная область|народная республика|область|край|респ\.?|г\.\s*/gi, "")
    .replace(/[^а-яё\s-]/gi, "")
    .replace(/\s+/g, " ")
    .trim();
}

const NORMALIZED_CAPITALS = Object.entries(REGION_CAPITALS).map(([region, capital]) => ({
  region,
  capital,
  key: normalizeRegionKey(region),
}));

// Returns the capital city for a region mentioned informally in uploaded
// data, or null if nothing matches closely enough.
export function findCapitalForRegion(regionHint: string): string | null {
  const key = normalizeRegionKey(regionHint);
  if (!key) return null;

  const exact = NORMALIZED_CAPITALS.find((r) => r.key === key);
  if (exact) return exact.capital;

  const partial = NORMALIZED_CAPITALS.find((r) => r.key.includes(key) || key.includes(r.key));
  return partial?.capital ?? null;
}
